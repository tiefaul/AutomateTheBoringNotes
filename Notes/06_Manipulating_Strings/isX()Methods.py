spam = "Testing"

spam.isalpha() # Returns True if the string consists only of letters and isn't blank
spam.isalnum() # Returns True if the string consists only of letters and numbers and is not blank
spam.isdecimal() # Returns True if the string consists only of numberic characters
spam.isspace() # Returns True if the string consists only of spaces, tabs, and newlines
spam.istitle() # Returns True if the string consists only of words that begin with an upper-case letter followed by only lowercase letters
spam.isnumeric() # Returns True if the string consists of only numbers