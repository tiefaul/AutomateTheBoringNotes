spam = ['cat', 'dog', 'mouse']
print(spam)

spam.append('moose') # adds moose to the list
print(spam)

spam.insert(1, 'chicken') # Inserts chicken to the 1 spot in spam 
print(spam)

spam.remove('dog') # Removes dog from the spam list
print(spam)

del spam[1] # Delete chicken
print(spam)